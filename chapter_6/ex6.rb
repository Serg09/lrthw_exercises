# variables
types_of_people = 10
# variable equal to string with another variable in a string
x = "There are #{types_of_people} types of people."
# variables equals to string
binary = 'binary'
do_not = 'don\'t'
# variable (string in a string. 2 places)
y = "Those who know #{binary} and those who #{do_not}."

# prints variables (strings)
puts x
puts y

# prints strings with variables which are the strings (2 places)
puts "I said: #{x}."
puts "I also said: '#{y}'."

# logic variable set to false
hilarious = false
# variable equals string with another variable
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# prints variables which is combination of string and logic variable
puts joke_evaluation

# variables equals strings
w = 'This is the left side of...'
e = 'a string with a right side.'

# prints sum of to variables (strings)
puts w + e
